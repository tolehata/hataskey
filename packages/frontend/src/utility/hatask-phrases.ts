export interface PhraseContext {
  pendingTaskCount: number;
  totalTaskCount: number;
  todayEventCount: number;
  todayEventTitle?: string;
  recentMoodAvg: number;
}

export function getPhrase(ctx?: PhraseContext): string {
  const now = new Date();
  const h = now.getHours();
  const m = now.getMonth();
  const d = now.getDate();
  const dow = now.getDay();
  const dateKey = `${m + 1}-${d}`;
  if (specialDays[dateKey] && Math.random() < 0.25) {
    const arr = specialDays[dateKey];
    return arr[Math.floor(Math.random() * arr.length)];
  }
  let base: string[];
  if (h < 5) base = dawn; else if (h < 11) base = morning; else if (h < 17) base = noon; else if (h < 21) base = evening; else base = night;
  const candidates: string[] = [...base];
  const mp = seasonal[m + 1]; if (mp) candidates.push(...mp);
  const dp = weekday[dow]; if (dp) candidates.push(...dp);
  if (Math.random() < 0.25) candidates.push(...trivia);
  candidates.push(...random);
  let msg = candidates[Math.floor(Math.random() * candidates.length)];
  if (ctx && Math.random() < 0.35) {
    const extras: string[] = [];
    if (ctx.pendingTaskCount >= 5) extras.push(contextPhrases.manyTasks[Math.floor(Math.random() * contextPhrases.manyTasks.length)]);
    else if (ctx.pendingTaskCount >= 3) extras.push(contextPhrases.someTasks[Math.floor(Math.random() * contextPhrases.someTasks.length)]);
    else if (ctx.pendingTaskCount === 0 && ctx.totalTaskCount > 0) extras.push(contextPhrases.allDone[Math.floor(Math.random() * contextPhrases.allDone.length)]);
    if (ctx.todayEventCount >= 2) extras.push(contextPhrases.busyDay[Math.floor(Math.random() * contextPhrases.busyDay.length)]);
    else if (ctx.todayEventCount === 1 && ctx.todayEventTitle) extras.push(`今日の予定: ${ctx.todayEventTitle}`);
    if (ctx.recentMoodAvg > 0 && ctx.recentMoodAvg < 2) extras.push(contextPhrases.lowMood[Math.floor(Math.random() * contextPhrases.lowMood.length)]);
    else if (ctx.recentMoodAvg >= 4.5) extras.push(contextPhrases.goodMood[Math.floor(Math.random() * contextPhrases.goodMood.length)]);
    if (extras.length > 0) msg += '\n' + extras[Math.floor(Math.random() * extras.length)];
  }
  return msg;
}

export function getPhraseCount(): number {
  return dawn.length + morning.length + noon.length + evening.length + night.length +
    trivia.length + random.length +
    Object.values(seasonal).reduce((s, a) => s + a.length, 0) +
    Object.values(weekday).reduce((s, a) => s + a.length, 0) +
    Object.values(specialDays).reduce((s, a) => s + a.length, 0) +
    Object.values(contextPhrases).reduce((s, a) => s + a.length, 0);
}
